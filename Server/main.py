def models():
    return {"models": ["asm"]}


@app.post("/models/asm")
def asm(config: ModelConfig):
    print(config)
    token_ids = generate(
        model=model,
        idx=text_to_token_ids(config.start_text, tokenizer),
        max_new_tokens=config.max_new_tokens,
        context_size=GPT_CONFIG_124M["context_length"],
        top_k=25,
        temperature=config.temp,
    )
    return {"text": token_ids_to_text(token_ids, tokenizer)}

